function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function getRandomQuestions(questions, count = 10) {
    shuffleArray(questions);
    return questions.slice(0, count);
}

const uniqueQuestions = getRandomQuestions(questions, 10);
let currentQuestionIndex = 0;
let score = 0;
const container_questions = document.getElementById('container_questions');
const questionAnswer = document.getElementById('question-answer');
const submitButton = document.getElementById('submit-button');
const noQuestionDisplay = document.getElementById('noQuestion');

function displayQuestion() {
    container_questions.innerHTML = "";
    questionAnswer.innerHTML = "";
    const selectedQuestionObj = uniqueQuestions[currentQuestionIndex];
    const selectedQuestion = selectedQuestionObj.question;
    const options = selectedQuestionObj.options;
    noQuestionDisplay.textContent = `${currentQuestionIndex + 1} / ${uniqueQuestions.length}`;

    const h3 = document.createElement('h3');
    h3.classList.add('text-sm', 'sm:text-lg', 'text-gray-600', 'text-center');
    h3.textContent = selectedQuestion;
    container_questions.appendChild(h3);  

    let optionIndex = 1;
    for (const optionKey in options) {
        const div = document.createElement('div');
        div.classList.add('flex', 'items-center', 'gap-x-3', 'p-3', 'border', 'border-gray-300', 'rounded-md', 'shadow-sm', 'hover:bg-gray-100', 'transition-all', 'duration-200', 'cursor-pointer');
        
        const input = document.createElement('input');
        input.type = 'radio';
        input.name = 'answers';
        input.id = `radio${optionIndex}`;
        input.classList.add('form-radio', 'h-5', 'w-5', 'text-green-500', 'focus:ring-green-500');

        const label = document.createElement('label');
        label.htmlFor = `radio${optionIndex}`;
        label.classList.add('text-lg', 'font-semibold', 'cursor-pointer');
        label.textContent = options[optionKey];
        
        div.appendChild(input);
        div.appendChild(label);
        questionAnswer.appendChild(div);
        
        optionIndex++;
    }
}

submitButton.addEventListener('click', () => {
    const selectedOption = document.querySelector('input[name="answers"]:checked');
    
    if (selectedOption) {
        const selectedAnswerId = selectedOption.id;
        const selectedAnswerKey = String.fromCharCode(65 + parseInt(selectedAnswerId.replace('radio', '')) - 1);
        const selectedQuestion = uniqueQuestions[currentQuestionIndex];
        
        if (selectedAnswerKey === selectedQuestion.correctAns) {
            score += 10;
        }
        
        currentQuestionIndex++;
        if (currentQuestionIndex < uniqueQuestions.length) {
            displayQuestion();
        } else {
            // Quiz is finished, display score
            container_questions.innerHTML = `   <div class="flex flex-col items-center">
            <h3 class='text-lg text-center animate-bounce'>Quiz Completed!</h3>
            <div class="flex items-center space-x-2">
                <svg xmlns="http://www.w3.org/2000/svg" class="h-8 w-8 text-yellow-500 animate-pulse" fill="currentColor" viewBox="0 0 24 24"><path d="M12 .587l3.668 7.431 8.26 1.206-5.979 5.421 1.411 8.236L12 18.896l-7.36 3.865 1.411-8.236-5.979-5.421 8.26-1.206L12 .587z"/></svg>
                <p class='text-lg text-center'>Your score: <span class="font-bold">${score}</span> / 100</p>
            </div>
        </div>`;
            questionAnswer.innerHTML = "";
            submitButton.disabled = true;
        }           
        if(currentQuestionIndex + 1 < uniqueQuestions.length){
            noQuestionDisplay.textContent = `${currentQuestionIndex + 1} / ${uniqueQuestions.length}`;
        }
    } else {
        alert("Please select an answer before submitting.");
    }
});
displayQuestion();


